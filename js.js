document.getElementById("btn1").addEventListener("click", function()

{
    var gallery1=document.getElementById("nature");
    if (gallery1.style.display=="none")
    {
gallery1.style.display = "block";
    }
    else
    {
        gallery1.style.display="none";
    }
})

